package day05operators;

public class IncerementDecrement {

	public static void main(String[] args) {

		
		//int data tipinde bir variable olusturun 
		
		// bu variablen  degerini carpma ve bolme kullanarak artirin ve azaltin
		
		
		int num1=12;
		
		//uzun yol ile carpma  yaparak increment  
		
		num1=num1*3;// num1=36
		
		System.out.println(num1);
		
		//kisa yol 
		
		num1*=3;//num1 =36*3=108
		
		System.out.println(num1);
		
		
		// Bolme 
		
		
		// uzun yolla  bolme  yaparak decrement 
		
		
		num1=num1/9;
		//108/9=12
		System.out.println(num1);
		
		//kisa yol
		
		num1/=2;//12/2=6
		
		System.out.println(num1);
		
		
		
		
		
	}

}
