package day05operators;

public class Increment04 {

	public static void main(String[] args) {
		// int  data type  inda bir tane variable  olusturun
		// Bu variable  i her sererinde  1  artirmka  icn 3 yontemi  de kullanin
		
		
		int r1=10;
		
		//uzun yol 
		r1=r1+1;//11
		
		System.out.println(r1);//r1=11
		
		//kisa yol
		r1+=1;//12
		System.out.println(r1);//r1=12
		
		
		
		// en kisa yol
		r1++;//13
		System.out.println(r1);//r1=13
		

	}

}
