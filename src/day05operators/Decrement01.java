package day05operators;

public class Decrement01 {

	public static void main(String[] args) {
		//int  data type  in da  bir  variable  olusturun.
		// Bu variablen  degerini  uc  yonte mi de kullanarak her defasinda bir azaltin
	
		
		//uzun  yol
		int num1=12;
		num1=num1-1;
		System.out.println(num1);// 11
		
		
		//kisa yol
		num1-=1;// 10 a  donusecek 
		System.out.println(num1);
		
		
		// en kisa yol 
		num1--;
		System.out.println(num1);// 9
		
		
		
		
		
	}

}
