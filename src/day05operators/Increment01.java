package day05operators;

public class Increment01 {

	public static void main(String[] args) {
		//int data  type'inda iki variable  olusturun
		// birinci  3, ikinciyi 7 artirin
		
		
		int r1=12;
		System.out.println(r1);//12
		r1=r1+3;
		System.out.println(r1);//17
		
		
		int s2=12;
		s2=s2+7;
		System.out.println(s2);
		
		

	}

}
